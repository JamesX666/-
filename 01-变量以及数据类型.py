# 数据类型的概念
# 在python里数据都有各自对应的类型


print(45)  # int 整数类型
print(3.145)  # float 类型
print((-1) ** 0.5)  # complex类型

# 字符串类型：其实就是一段普通的文字
# python 里的字符串要求i使用一对单引号，或者双引号来包裹
print('今天天气好晴朗，处处好好风光')
print('56')

# 布尔类型：用来表示真假/对错
# 布尔类型只有两个值，False或True
print(4 > 3)  # 对
print(1 > 5)  # 错

# 列表类型
